print("\nPROGRAM TUGAS KONVERSI\n")

print("-----------Fahrenheit-------------")
suhu = float(input("Masukkan Suhu : "))

#Tugas 1 Fahrenheit ke Kelvin
konvCelcius = 5/9 * (suhu - 32)
print("Hasil Konversi Fahrenheit ke Celcius : ", konvCelcius)

hitKon = konvCelcius + 273
print("Hasil Konversi Fahrenheit ke Kelvin : ", hitKon)
